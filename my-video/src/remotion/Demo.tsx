const useRise = (delay = 0, mass = 0.6) => {
  const frame = useCurrentFrame();
  const { fps } = useVideoConfig();
  const s = spring({
    frame: frame - delay,
    fps,
    config: { damping: 200, mass },
  });
  return {
    opacity: s,
    transform: `translateY(${interpolate(s, [0, 1], [24, 0])}px)`,
  };
};

