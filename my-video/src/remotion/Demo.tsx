// title — neural network forming a brain
const BrainArt: React.FC = () => {
  const frame = useCurrentFrame();
  const nodes = React.useMemo(() => {
    const pts: { x: number; y: number; r: number }[] = [];
    const rings = [
      { count: 1, rad: 0 },
      { count: 6, rad: 120 },
      { count: 12, rad: 230 },
      { count: 16, rad: 340 },
    ];
    rings.forEach((ring, ri) =>
      Array.from({ length: ring.count }).forEach((_, i) => {
        const a = (i / ring.count) * Math.PI * 2 + ri * 0.4;
        pts.push({
          x: 500 + Math.cos(a) * ring.rad,
          y: 500 + Math.sin(a) * ring.rad * 0.85,
          r: 6 + ((i * 7 + ri * 3) % 5),
        });
      }),
    );
    return pts;
  }, []);
  return (
    <ArtWrap>
      {nodes.map((n, i) =>
        nodes
          .slice(i + 1)
          .filter((m) => Math.hypot(m.x - n.x, m.y - n.y) < 180)
          .map((m, j) => (
            <line
              key={`${i}-${j}`}
              x1={n.x}
              y1={n.y}
              x2={m.x}
              y2={m.y}
              stroke={CYAN}
              strokeWidth={1}
              opacity={0.12 + 0.1 * Math.sin(frame / 12 + i)}
            />
          )),
      )}
      {nodes.map((n, i) => {
        const pulse = 1 + 0.25 * Math.sin(frame / 10 + i * 0.6);
        return (
          <circle
            key={i}
            cx={n.x}
            cy={n.y}
            r={n.r * pulse}
            fill={i % 3 === 0 ? VIOLET : CYAN}
            opacity={0.85}
          />
        );
      })}
    </ArtWrap>
  );
};

