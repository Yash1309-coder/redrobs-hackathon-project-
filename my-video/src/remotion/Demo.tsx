const Scene: React.FC<{
  dur: number;
  id?: string;
  audio?: string;
  image?: string | null;
  children: React.ReactNode;
  bg?: string;
}> = ({ dur, id, audio, image, children, bg = BG }) => {
  const frame = useCurrentFrame();
  const inOpacity = interpolate(frame, [0, 14], [0, 1], {
    extrapolateRight: "clamp",
  });
  const outOpacity = interpolate(frame, [dur - 14, dur], [1, 0], {
    extrapolateLeft: "clamp",
  });
  // slow Ken-Burns drift so the illustration feels alive
  const scale = interpolate(frame, [0, dur], [1.08, 1.16]);
  return (
    <AbsoluteFill
      style={{
        backgroundColor: bg,
        opacity: Math.min(inOpacity, outOpacity),
        fontFamily: FONT,
        color: FG,
      }}
    >
      {audio ? <Audio src={staticFile(audio)} /> : null}
      {image ? (
        <AbsoluteFill>
          <Img
            src={staticFile(image)}
            style={{
              width: "100%",
              height: "100%",
              objectFit: "cover",
              transform: `scale(${scale})`,
              opacity: 0.4,
            }}
          />
          <AbsoluteFill
            style={{
              background:
                "radial-gradient(closest-side, rgba(11,16,32,0.30), rgba(11,16,32,0.88))",
            }}
          />
        </AbsoluteFill>
      ) : id ? (
        <SceneArt id={id} />
      ) : null}
      <Glow />
      {children}
    </AbsoluteFill>
  );
};

